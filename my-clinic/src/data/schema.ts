import {
  pgTable,
  integer,
  serial,
  varchar,
  boolean,
  text,
} from "drizzle-orm-pg";

export const clinics = pgTable("clinics", {
  name: varchar("long_name_version", { length: 60 }).notNull(),
  registration: varchar("registration_link", { length: 256 }).notNull(),
  psm: varchar("pms", { length: 20 }),
  metaTitle: varchar("meta_title", { length: 100 }),
  metaDescription: varchar("meta_description", { length: 256 }),
  slug: varchar("slug", { length: 100 }),
  website: varchar("website", { length: 80 }).notNull(),
  shortName: varchar("clinic_name"),
  isDisplay: boolean("display_on_web"),
  clinicSuburbLink: varchar("link_to_clinic_suburb_page"),
  address: varchar("full_address", { length: 256 }).notNull(),
  city: varchar("city", { length: 50 }).notNull(),
  suburb: varchar("suburb", { length: 50 }).notNull(),
  state: varchar("state", { length: 20 }).notNull(),
  postcode: varchar("postcode", { length: 10 }).notNull(),
  email: varchar("email", { length: 50 }),
  phone: varchar("phone", { length: 30 }).notNull(),
  nearby1: varchar("nearby1_txt", { length: 40 }),
  nearby1Link: varchar("nearby1_link", { length: 40 }),
  nearby2: varchar("nearby2_txt", { length: 40 }),
  nearby2Link: varchar("nearby2_link", { length: 40 }),
  nearby3: varchar("nearby3_txt", { length: 40 }),
  nearby3Link: varchar("nearby3_link", { length: 40 }),
  nearby4: varchar("nearby4_txt", { length: 40 }),
  nearby4Link: varchar("nearby4_link", { length: 40 }),
  about: text("about_clinic").notNull(),
});

export const cities = pgTable("cities", {
  citySlug: varchar("city_slug", { length: 40 }),
  name: varchar("city_name", { length: 50 }),
  state: varchar("state", { length: 20 }),
  metaTitle: varchar("meta_title", { length: 100 }),
  metaDescroption: varchar("meta_description", { length: 256 }),
  h1: varchar("h1", { length: 256 }),
  h2: varchar("h2", { length: 256 }),
  subHeadingText: varchar("sub_heading_text", { length: 256 }),
  tick1: varchar("tick_1", { length: 256 }),
  tick2: varchar("tick_2", { length: 256 }),
  tick3: varchar("tick_3", { length: 256 }),
  aboutBookphysio: text("about_bookphysio"),
});

export const suburbs = pgTable("suburbs", {
  id: serial("suburb_id").primaryKey(),
  slug: varchar("suburb_slug", { length: 50 }),
  name: varchar("suburb_name", { length: 50 }),
  city: varchar("city", { length: 50 }),
  state: varchar("state", { length: 20 }),
  postcode: integer("postcode"),
  metaTitle: varchar("meta_title", { length: 100 }),
  metaDescription: varchar("meta_description", { length: 256 }),
  h1: varchar("h1", { length: 256 }),
  h2: varchar("h2", { length: 256 }),
  aboutBookphysio: text("about_bookphysio"),
  nearby1Link: varchar("nearby1_link", { length: 40 }),
  nearby1: varchar("nearby1_txt", { length: 40 }),
  nearby1State: varchar("nearby1_state", { length: 40 }),
  nearby1Postcode: integer("nearby1_postcode"),
  nearby2Link: varchar("nearby2_link", { length: 40 }),
  nearby2: varchar("nearby2_txt", { length: 40 }),
  nearby2State: varchar("nearby2_state", { length: 40 }),
  nearby2Postcode: integer("nearby2_postcode"),
  nearby3Link: varchar("nearby3_link", { length: 40 }),
  nearby3: varchar("nearby3_txt", { length: 40 }),
  nearby3State: varchar("nearby3_state", { length: 40 }),
  nearby3Postcode: integer("nearby3_postcode"),
  nearby4Link: varchar("nearby4_link", { length: 40 }),
  nearby4: varchar("nearby4_txt", { length: 40 }),
  nearby4State: varchar("nearby4_state", { length: 40 }),
  nearby4Postcode: integer("nearby4_postcode"),
});
